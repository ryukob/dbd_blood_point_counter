<template>
  <div>
      <div class="current-level" >
          {{level}} Level
      </div>
      <div class="slider">
          <input type="range" min="1" max="40" step="1" v-model="level">
      </div>
  </div>
</template>

<script>
import {SliderEvent} from '../SliderEvent.js'

export default {
    data(){
        return{
            level:1
        }
    },

    watch:{
        level: function(val){
                SliderEvent.$emit('level-change',val);
        }
    }

}
</script>

